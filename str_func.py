def str_upp(user_str):
    """
    :param user_str: строка
    :return: строку в верхнем регистре
    """
    return user_str.upper()

def str_tit(user_str):
    """
    :param user_str: строка
    :return: строку, каждое слово с заглавной буквы
    """
    return user_str.title()

